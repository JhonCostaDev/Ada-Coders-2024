# Escreva um algoritmo que calcule (x + y) * (x + y). Os valores de x e y devem ser inseridos pelo usuário

print("====== Expressão Matemática ======")

x = float(input("Digite um número\n"))
y = float(input("Digite um outro número\n"))

resultado = (x + y) * (x + y)

print(f"O resultado dos números {x} e {y} na expressão (x + y) * (x + y) é: {resultado}")
